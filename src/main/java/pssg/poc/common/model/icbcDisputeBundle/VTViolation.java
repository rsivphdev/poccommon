package pssg.poc.common.model.icbcDisputeBundle;

/**
 * The Class VTViolation.
 * @author HLiang
 */
public class VTViolation {

    /** The count number. */
    private Number count_number  = null;
    
    /** The act. */
    private String act  = null;
    
    /** The ticketed amount. */
    private Number ticketed_amount  = null;
    
    /** The compressed section. */
    private String compressed_section  = null;
    
    /** The disputing allegation. */
    private Boolean disputing_allegation  = null;
    
    /**
     * Gets the count number.
     *
     * @return the count number
     */
    public Number getCount_number() {
        return count_number;
    }

    /**
     * Sets the count number.
     *
     * @param count_number the new count number
     */
    public void setCount_number(Number count_number) {
        this.count_number = count_number;
    }

    /**
     * Gets the act.
     *
     * @return the act
     */
    public String getAct() {
        return act;
    }

    /**
     * Sets the act.
     *
     * @param act the new act
     */
    public void setAct(String act) {
        this.act = act;
    }

    
    /**
     * Gets the ticketed amount.
     *
     * @return the ticketed amount
     */
    public Number getTicketed_amount() {
        return ticketed_amount;
    }

    /**
     * Sets the ticketed amount.
     *
     * @param ticketed_amount the new ticketed amount
     */
    public void setTicketed_amount(Number ticketed_amount) {
        this.ticketed_amount = ticketed_amount;
    }

    
    /**
     * Gets the compressed section.
     *
     * @return the compressed section
     */
    public String getCompressed_section() {
        return compressed_section;
    }

    /**
     * Sets the compressed section.
     *
     * @param compressed_section the new compressed section
     */
    public void setCompressed_section(String compressed_section) {
        this.compressed_section = compressed_section;
    }

    /**
     * Gets the disputing allegation.
     *
     * @return the disputing allegation
     */
    public Boolean getDisputing_allegation() {
        return disputing_allegation;
    }

    /**
     * Sets the disputing allegation.
     *
     * @param disputing_allegation the new disputing allegation
     */
    public void setDisputing_allegation(Boolean disputing_allegation) {
        this.disputing_allegation = disputing_allegation;
    }
    

}



